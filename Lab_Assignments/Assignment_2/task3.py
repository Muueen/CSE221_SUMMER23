def merge(a, b):
    new = []
    i = 0
    j = 0
    while i<len(a) and j<len(b):
        if a[i] < b[j]:
            new.append(a[i])
            i += 1
        else:
            new.append(b[j])
            j += 1
    while i < len(a):
        new.append(a[i])
        i += 1
    while j < len(b):
        new.append(b[j])
        j += 1
    return new

def mergeSort(arr):
    if len(arr) <= 1:
        return arr
    else:
        mid = len(arr)//2
        a1 = mergeSort(arr[mid:])
        a2 = mergeSort(arr[:mid])
        return merge(a1, a2)

